public class Q7_lab1 {
    public static void main(String[] args) {
        int a= 10;
        int b=20;
        System.out.println("Before swapping value a = "+ a +" b = "+ b);
        int temp;
        temp=a;
        a=b;
        b=temp;
        System.out.println("After swapping value a = "+ a +" b = "+ b);
    }
}
